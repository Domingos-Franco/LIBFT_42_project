#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	len;

	i = 0;
	while (i < size)
	{
		dst[i] = src[i];
		i++;
	}
	len = ft_strlen(dst);
	return (len);
}
